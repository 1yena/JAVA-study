package application;

public class Increment {

	public static void main(String[] args) {
		//정수값을 1씩 증가 1씩 감소 => 반복문
		
		int count = 10;
		
		count = count-1;
		System.out.println(count);
		
		count -= 1;
		System.out.println(count);
		
		count --;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		--count;
		System.out.println(count);
		
		System.out.println("=================================");
		
		--count;
		System.out.println(count);
		
		System.out.println("=================================");
		
		int cont = 0;
		
		cont = cont+1;
		System.out.println(cont);
		
		cont += 1;
		System.out.println(cont);
		
		cont ++;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);
		
		++cont;
		System.out.println(cont);

	}

}
