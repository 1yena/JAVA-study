package yn;


public class Person {
	
	private String name;
	private double height;
	
	public Person() {
		System.out.println("한 사람을 생성");
	}

	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}
	
	
	
	
	

}
