package yn;

public class While {

	public static void main(String[] args) {
		int coun = 0;

		coun = coun + 1;
		System.out.println(coun);
		
		coun +=1;
		System.out.println(coun);
		
		coun ++;
		System.out.println(coun);
		
		coun ++;
		System.out.println(coun);
		
		System.out.println("====================");
		
		++coun;
		System.out.println(coun);
		
		System.out.println("====================");
		
		int cont = 5; 
		
		cont = cont - 1;
		System.out.println(cont);
		
		cont -=1;
		System.out.println(cont);
		
		cont --;
		System.out.println(cont);
		
		--cont;
		System.out.println(cont);
			
		}
	
}
		



